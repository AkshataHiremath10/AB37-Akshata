package switchhh;
/* WAP using Switch(3)keyword case 1:addition, case 2:subtraction, case 3:multiplication,
   case 4:division and break at case(3). */

public class Assignment_40 {
	Assignment_40() {
		int a = 1;
		switch (a) {
		case 1:
			System.out.println("addition");
		case 2:
			System.out.println("subtraction");
		case 3:
			System.out.println("multiplication");
			break;
		case 4:
			System.out.println("division");
		}
	}

	public static void main(String[] args) {
		{
			new Assignment_40();
		}
	}
}
