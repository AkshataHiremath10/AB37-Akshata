// WAP to execute the while loop
public class Assignment_97 {
	public static void main(String[] args) {
		int age = 18; // dec and init
		while (age >= 10) // condition
		{
			int current_age = 1 + age;
			System.out.println(current_age);
			age--; // decrement operator

		}

	}

}
