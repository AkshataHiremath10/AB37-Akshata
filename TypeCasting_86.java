package castingg;
//WAP to convert weight from int to double and from double to int

public class TypeCasting_86 {
	public static void main(String[] args) {
		double myweight = 90; // widening
		System.out.println(myweight);

		int yourweight = (int) 90.0;  //narrowing
		System.out.println(yourweight);

	}

}
